import numpy as np

# Mảng a
a = np.array([10, 1, 1, 9, 12, 1, 9, 12, 10])
print('Phần tử của mảng a:', a)
print('Giá trị trung bình:', a.mean())
print('Độ lệch chuẩn:', a.std())

